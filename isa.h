#pragma once
// ============================================================================
// 🖥️ 极简指令集架构 (Minimal ISA) — 数字生命培养皿
// ============================================================================
//
// 本文件定义了数字生命虚拟机的完整指令集。设计目标：
//   1. 极简 — 共 35 条指令，操作码用 8 位编码，编码空间充裕
//   2. 完备 — 覆盖通用计算（算术/逻辑/跳转/栈操作）+ 生命行为指令
//   3. 可变异 — 随机字节序列也能被解释为有效程序（未知操作码=NOP）
//   4. 紧凑 — 大多数指令仅 1-2 字节，支持可变长立即数
//
// 指令分类：
//   ┌──────────────────┬──────────┬──────────────────────────────────┐
//   │ 类别             │ 条数     │ 说明                             │
//   ├──────────────────┼──────────┼──────────────────────────────────┤
//   │ 数据传送         │ 4        │ MOV, PUSH, POP, LOAD, STORE       │
//   │ 算术运算         │ 6        │ ADD, SUB, MUL, DIV, INC, DEC      │
//   │ 逻辑运算         │ 6        │ AND, OR, XOR, NOT, SHL, SHR       │
//   │ 比较             │ 1        │ CMP (设置 Zero/Negative 标志)     │
//   │ 控制流           │ 9        │ JMP, JE, JNE, JG, JL, JGE, JLE,   │
//   │                  │          │ CALL, RET                         │
//   │ 数字生命行为     │ 6        │ REPLICATE, EAT, FIGHT, SENSE,     │
//   │                  │          │ MOVE, DIE                         │
//   │ 其他             │ 2        │ NOP, HALT                         │
//   └──────────────────┴──────────┴──────────────────────────────────┘
//
// 寄存器模型：
//   - R0-R7：8 个通用 32 位寄存器（指令中编码为 3 位）
//   - R0 惯用作返回值（类似 x86 EAX）
//   - SENSE 指令将环境信息填入全部 R0-R7
//   - IP/SP/FLAGS 是特殊寄存器，不可被 MOV 等指令直接寻址
//
// 指令编码（变长）：
//   1 字节指令：NOP, RET, HALT, DIE
//   2 字节指令：OPCODE + [Rd(3b)|Rs(3b)|mode(2b)] 如 INC, DEC, NOT
//   3 字节指令：OPCODE + Rd + 1字节立即数 如 JMP, SENSE, MOVE
//   4 字节指令：OPCODE + Rd + mode + 2字节立即数 如 MOV Rd, LargeImm
//   立即数支持 7 位(≤127)或 14 位(≤16383)的变长编码（高位连续置1）
// ============================================================================

#include <cstdint>

namespace ISA {

// ============================================================================
// 操作码枚举 — 每条指令的唯一 8 位标识
// ============================================================================

enum Opcode : uint8_t {
    // ── 基础指令 (0x00-0x0D) ────────────────────────────────────────────
    NOP       = 0x00,  ///< 空操作 — 不做任何事，占用一个指令周期
    MOV       = 0x01,  ///< 数据传送 — MOV Rd, Imm/Reg (mode.bit4=1→立即数, =0→寄存器)
    ADD       = 0x02,  ///< 加法 — Rd = Rd + Rs (32位有符号)
    SUB       = 0x03,  ///< 减法 — Rd = Rd - Rs
    MUL       = 0x04,  ///< 乘法 — Rd = Rd * Rs (可能溢出，取低32位)
    DIV       = 0x05,  ///< 除法 — Rd = Rd / Rs (Rs=0时跳过，避免除零崩溃)
    INC       = 0x06,  ///< 自增 — Rd = Rd + 1
    DEC       = 0x07,  ///< 自减 — Rd = Rd - 1
    AND_OP    = 0x08,  ///< 按位与 — Rd = Rd & Rs
    OR_OP     = 0x09,  ///< 按位或 — Rd = Rd | Rs (命名加_OP避免与C++关键字冲突)
    XOR_OP    = 0x0A,  ///< 按位异或 — Rd = Rd ^ Rs
    NOT_OP    = 0x0B,  ///< 按位取反 — Rd = ~Rd
    SHL       = 0x0C,  ///< 逻辑左移 — Rd = Rd << Imm (仅低5位有效, 0-31)
    SHR       = 0x0D,  ///< 算术右移 — Rd = Rd >> Imm (仅低5位有效)

    // ── 比较与控制流 (0x0E-0x15) ────────────────────────────────────────
    CMP       = 0x0E,  ///< 比较 — 计算 Rd-Rs，设置 FLAG_ZERO / FLAG_NEGATIVE，不修改寄存器
    JMP       = 0x0F,  ///< 无条件跳转 — IP = Addr (Addr 对 PROGRAM_MEMORY_SIZE 取模)
    JE        = 0x10,  ///< 相等则跳转 — 若 FLAG_ZERO=1，则 IP = Addr
    JNE       = 0x11,  ///< 不等则跳转 — 若 FLAG_ZERO=0，则 IP = Addr
    JG        = 0x12,  ///< 大于则跳转 — 若 Z=0 且 N=0（即 Rd > Rs），则 IP = Addr
    JL        = 0x13,  ///< 小于则跳转 — 若 FLAG_NEGATIVE=1（即 Rd < Rs），则 IP = Addr
    JGE       = 0x14,  ///< 大于等于跳转 — 若 FLAG_NEGATIVE=0（即 Rd >= Rs）
    JLE       = 0x15,  ///< 小于等于跳转 — 若 Z=1 或 N=1（即 Rd <= Rs）

    // ── 栈操作 (0x16-0x19) ──────────────────────────────────────────────
    PUSH      = 0x16,  ///< 压栈 — 将 Rs 的值压入栈顶，SP++
    POP       = 0x17,  ///< 弹栈 — 从栈顶弹出值到 Rd，SP--
    CALL      = 0x18,  ///< 子程序调用 — 将返回地址(当前IP)压栈，IP = Addr
    RET       = 0x19,  ///< 子程序返回 — 从栈顶弹出返回地址，IP = 弹出值

    // ── 内存访问 (0x1A-0x1B) ────────────────────────────────────────────
    LOAD      = 0x1A,  ///< 从数据内存加载 — Rd = DataMemory[Addr]（32位对齐）
    STORE     = 0x1B,  ///< 存入数据内存 — DataMemory[Addr] = Rs（32位对齐）

    // ── 数字生命专用指令 (0x20-0x25) ────────────────────────────────────
    // 这些指令与模拟世界交互，执行后会消耗能量并产生副作用
    REPLICATE = 0x20,  ///< 自我复制 — 创建基因变异的子实体，消耗 REPLICATION_COST 能量
    EAT       = 0x21,  ///< 进食 — 消耗当前位置食物获取能量，最多 FOOD_ENERGY_PER_BITE
    FIGHT     = 0x22,  ///< 攻击 — 向 R1 指定的方向发起战斗，赢家夺取输家能量
    SENSE     = 0x23,  ///< 感知环境 — 将周围实体/食物信息填入 R0-R7，消耗 SENSE_ENERGY_COST
    MOVE      = 0x24,  ///< 移动 — 向 R0 指定的方向移动一格，消耗 MOVE_ENERGY_COST
    DIE       = 0x25,  ///< 自我终结 — 立即死亡，剩余能量转化为食物，适合"利他"策略

    // ── 停机 (0xFF) ─────────────────────────────────────────────────────
    HALT      = 0xFF   ///< 停机 — 停止执行，IP不再前进。实体进入永久休眠状态
};

// ============================================================================
// 寄存器编码 — 3 位编码支持 8 个通用寄存器
// ============================================================================

enum Register : uint8_t {
    // 通用寄存器 (可通过指令直接读写)
    R0 = 0,  ///< 通用寄存器0 — 惯用作函数返回值 / SENSE:最近实体ID
    R1 = 1,  ///< 通用寄存器1 — SENSE:最近实体距离 / FIGHT:攻击方向
    R2 = 2,  ///< 通用寄存器2 — SENSE:最近实体能量
    R3 = 3,  ///< 通用寄存器3 — SENSE:最近实体方向
    R4 = 4,  ///< 通用寄存器4 — SENSE:当前位置食物量
    R5 = 5,  ///< 通用寄存器5 — SENSE:附近实体数量
    R6 = 6,  ///< 通用寄存器6 — SENSE:自身能量
    R7 = 7,  ///< 通用寄存器7 — SENSE:自身年龄

    // 特殊寄存器 — 不可被 MOV/算术指令直接寻址，由控制流指令隐式修改
    IP_REG = 8,       ///< 指令指针 (Instruction Pointer) — 指向下一条要执行的指令
    SP_REG = 9,       ///< 栈指针 (Stack Pointer) — 指向栈顶下一个空闲槽位
    FLAGS_REG = 10    ///< 标志寄存器 — 存储 CMP 指令的比较结果
};

// ============================================================================
// FLAGS 标志位 — CMP 指令设置的比较结果
// ============================================================================

enum Flags : uint8_t {
    FLAG_ZERO     = 1 << 0,  ///< 零标志 — CMP 结果为零 (Rd == Rs)
    FLAG_CARRY    = 1 << 1,  ///< 进位/借位标志 — 预留，当前未使用
    FLAG_NEGATIVE = 1 << 2,  ///< 负数标志 — CMP 结果为负 (Rd < Rs)
    FLAG_OVERFLOW = 1 << 3,  ///< 溢出标志 — 预留，当前未使用
};

// ============================================================================
// 辅助函数 — 调试与反汇编
// ============================================================================

/// @brief 获取寄存器名称字符串（用于反汇编输出）
/// @param r 寄存器编号 (0-7)
/// @return  "R0"~"R7"，越界则返回 "??"
inline const char* reg_name(uint8_t r) {
    static const char* names[] = {"R0","R1","R2","R3","R4","R5","R6","R7"};
    return (r < 8) ? names[r] : "??";
}

/// @brief 获取操作码的助记符名称（用于反汇编输出）
/// @param op 操作码值
/// @return  助记符字符串（如 "MOV", "SENSE"），未知操作码返回 "???"
/// @note    实现见 isa.cpp
inline const char* op_name(uint8_t op);

} // namespace ISA
