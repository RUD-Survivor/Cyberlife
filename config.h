#pragma once
// ============================================================================
// 🧬 全局配置常量 — 数字生命培养皿 (Digital Life Petri Dish)
// ============================================================================
//
// 本文件定义了整个模拟世界的所有可调参数。所有常量均为编译期确定（constexpr），
// 修改这些值会直接影响模拟的行为和性能特征。
//
// 设计理念：
//   - 世界是 64x64 的环面（toroidal）网格——上下、左右边界互相连接
//   - 能量是核心货币：移动、感知、复制、战斗都消耗能量
//   - 变异率是进化引擎：低能量 + 高代数 → 高变异率（压力诱导变异假说）
//   - 食物随机生成模拟环境资源，每 tick 有一定概率出现
//
// 关键调参指南：
//   - 增加 WORLD_WIDTH/HEIGHT → 更大世界，实体更分散，减少冲突
//   - 增加 FOOD_SPAWN_RATE → 更多食物，种群更容易存活和扩张
//   - 增加 MUTATION_RATE → 进化更快，但也可能导致基因崩溃（有害突变过多）
//   - 增加 REPLICATION_THRESHOLD → 复制门槛更高，种群增长更慢，更稳定
//   - 增加 TICKS_PER_STEP → 每 tick 执行更多指令，实体行为更"智能"
// ============================================================================

#include <cstdint>
#include <cstdlib>

// ============================================================================
// 辅助宏
// ============================================================================

/// @brief 将值 x 钳制在 [lo, hi] 区间内
/// @details 确保数值不超出合法范围，广泛用于坐标、能量值、寄存器值等场景
/// @param x  输入值
/// @param lo 下限（含）
/// @param hi 上限（含）
/// @return   钳制后的值
#define DL_CLAMP(x, lo, hi) (((x) < (lo)) ? (lo) : (((x) > (hi)) ? (hi) : (x)))

/// @brief 环面坐标包装（toroidal wrapping）
/// @details 将坐标 v 包装在 [0, max) 范围内，实现世界边界的无缝连接。
///          使用双重取模确保负数也能正确包装。
///          例如：-1 在 max=64 的世界中变为 63（从右边界绕回）。
/// @param v   原始坐标值（可为负数）
/// @param max 坐标轴的最大值（不含）
/// @return    包装后的坐标，保证在 [0, max) 范围内
#define DL_WRAP_COORD(v, max) (((v) % (max) + (max)) % (max))

namespace Config {

// ============================================================================
// 🌍 世界参数 — 控制模拟环境的物理尺度和容量
// ============================================================================

/// @brief 世界宽度（X轴格点数）
/// @note 世界使用环面拓扑（toroidal topology），水平方向左右边界相连
constexpr int WORLD_WIDTH          = 2048;

/// @brief 世界高度（Y轴格点数）
/// @note 环面拓扑使垂直方向上下边界相连
constexpr int WORLD_HEIGHT         = 2048;

/// @brief 最大实体容量（所有内存池预分配的上限）
/// @details 超过此数量将无法创建新实体。使用数组而非动态分配以避免运行时开销。
///          512 个实体在 2048x2048 世界（4,194,304格）中约占 0.0122% 的密度上限。
constexpr int MAX_ENTITIES         = 512;

/// @brief 单格最大食物堆积量
/// @details 设置上限防止食物在单一格子上无限累积（如死亡高发区域），
///          使食物分布更加均匀，避免"富者愈富"的正反馈。
constexpr int MAX_FOOD_PER_TILE    = 1500;

// ============================================================================
// 🖥️ 虚拟机 (VM) 参数 — 控制每个数字生命的"大脑"能力
// ============================================================================

/// @brief 程序内存大小（字节），即基因组最大长度
/// @details 每个实体最多 4096 字节的程序存储。依据指令编码方式，
///          大约可容纳 1024~2048 条指令（部分指令需要额外的立即数字节）。
///          这是数字生命的"DNA"——复制时变异在此区域发生。
constexpr int PROGRAM_MEMORY_SIZE  = 4096;

/// @brief 调用栈深度（槽位数，每个槽位 4 字节 = int32_t）
/// @details 支持 CALL/RET 指令进行子程序调用。1024 层嵌套对复杂行为已足够。
///          栈内存池大小 = STACK_SIZE * sizeof(int32_t) = 4,096 字节/实体。
constexpr int STACK_SIZE           = 1024;

/// @brief 数据内存大小（字节），通用读写存储区
/// @details 通过 LOAD/STORE 指令访问。实体可在此保存计算中间值或行为状态。
///          65536 字节可存储 16,384 个 int32_t 值（按 4 字节对齐）。
constexpr int DATA_MEMORY_SIZE     = 65536;

/// @brief 每 tick 每实体最多执行的指令条数
/// @details 限制执行数量是为了：
///          1. 防止单个实体在一个 tick 内"跑飞"（无限循环）
///          2. 控制整体模拟速度
///          3. 给所有实体均等的 CPU 时间
///          16 条指令对一个简单的"感知→判断→行动"循环通常足够。
constexpr int TICKS_PER_STEP       = 16;

/// @brief 通用寄存器数量（R0 到 R7）
/// @details 8 个 32 位寄存器。R0 惯常用作函数返回值（类似 x86 的 EAX）。
///          SENSE 指令会填充全部 8 个寄存器（R0=最近实体ID, ..., R7=自身年龄）。
constexpr int NUM_REGISTERS        = 8;

// ============================================================================
// ⚡ 能量参数 — 数字生命的"经济系统"
// ============================================================================

/// @brief 新生成实体的初始能量值
/// @details 100 点能量足够实体存活若干 tick 并执行基础操作。
///          复制产生的子实体只获得一半（INITIAL_ENERGY/2 = 50 点）。
constexpr int INITIAL_ENERGY       = 100;

/// @brief 实体能量存储上限
/// @details 防止能量无限累积。500 点约相当于 5 倍初始能量，
///          为高能实体提供了足够的"战略储备"。
constexpr int MAX_ENERGY           = 500;

/// @brief 每 tick 基础代谢衰减（被动消耗）
/// @details 模拟维持生命的基本能量消耗。实际衰减量会根据年龄倍增：
///          actual = ENERGY_DECAY_RATE * (1.0 + (age / MAX_AGE) * 2.0)
///          年轻实体：~1 点/tick，年老实体：~3 点/tick
constexpr int ENERGY_DECAY_RATE    = 1;

/// @brief 移动一格消耗的能量值
/// @details 移动是主动行为，消耗高于被动代谢（模拟运动能耗）
constexpr int MOVE_ENERGY_COST     = 2;

/// @brief 复制的基础能量消耗
/// @details 实际消耗 = REPLICATION_COST + parentEnergy * 0.2
///          即 50 + 父体当前能量的 20%。高能量实体复制代价更大，
///          这促进了资源的代际再分配，防止单个高能实体无限复制。
constexpr int REPLICATION_COST     = 50;

/// @brief 触发复制所需的最低能量阈值
/// @details 低于此能量的实体无法执行 REPLICATE 指令。
///          80 点的门槛设置在 INITIAL_ENERGY (100) 和 REPLICATION_COST (50) 之间，
///          确保复制后父体仍有足够能量继续存活。
constexpr int REPLICATION_THRESHOLD = 20;

/// @brief 发起战斗消耗的能量
/// @details 攻击者需要支付 10 点能量来发起战斗，无论胜负。
///          这是对战斗行为的"定价"——只对高能实体有吸引力。
constexpr int FIGHT_ENERGY_COST    = 5;

/// @brief 感知操作消耗的能量
/// @details 执行 SENSE 指令的花费。1 点的低消耗鼓励实体频繁感知环境，
///          从而促进复杂行为策略的进化。
constexpr int SENSE_ENERGY_COST    = 1;

// ============================================================================
// 🧬 变异参数 — 进化的核心驱动力
// ============================================================================

/// @brief 每个基因组字节的基础突变概率
/// @details 复制时，每个基因字节有 0.5% 的概率翻转一个随机位。
///          实际变异率会根据环境压力动态调整：
///          effective = MUTATION_RATE * energyFactor * generationFactor
///          - energyFactor = 1 + (1 - energyRatio) * 3  （低能→高变异，最高4倍）
///          - generationFactor = 1 + log(1 + generation) * 0.5  （高代→略高）
///          这种"压力诱导变异"模拟了真实生物学中的 SOS 响应。
constexpr double MUTATION_RATE     = 0.005;

/// @brief 每复制一次的基因插入概率
/// @details 在基因组随机位置插入一个随机字节，可能引入全新的指令或破坏现有指令。
///          0.05% 的低概率确保基因组长度不会爆炸式增长。
constexpr double INSERTION_RATE    = 0.0005;

/// @brief 每复制一次的基因删除概率
/// @details 从基因组随机位置删除一个字节，可能移除无用的"垃圾DNA"
///          但也可能破坏关键的生存指令。与插入率等同以保持基因组长度稳定。
constexpr double DELETION_RATE     = 0.0005;

// ============================================================================
// 🍎 食物参数 — 环境资源供给机制
// ============================================================================

/// @brief 每 tick 的食物生成概率密度
/// @details 实际生成 ≈ WORLD_WIDTH * WORLD_HEIGHT * FOOD_SPAWN_RATE ≈ 614 个食物单位/tick
///          每个食物单位在随机格子上生成，能量值在 [5, MAX_FOOD_ENERGY] 之间。
///          食物是环境的"阳光"——不依赖实体而独立存在的能量来源。
constexpr double FOOD_SPAWN_RATE   = 0.15;

/// @brief 单个食物块的最大能量值
/// @details 新生成的食物能量在 [5, 30] 区间均匀分布，
///          模拟环境中大小不一的可利用资源。
constexpr int MAX_FOOD_ENERGY      = 30;

/// @brief 每次进食操作可获取的最大能量
/// @details 限制单次 EAT 指令的收益，防止站在食物丰富的格子上瞬间回满能量。
///          实体需要多次进食才能完全利用一格中的所有食物，
///          这增加了能量获取的时间成本，使策略更加多样化。
constexpr int FOOD_ENERGY_PER_BITE = 150;

// ============================================================================
// ⌛ 寿命参数 — 生、老、病、死的法则
// ============================================================================

/// @brief 第 0 代实体的基础最大寿命（单位：ticks）
/// @details 后代每增加一代，最大寿命增加 50 ticks：
///          actualMaxAge = MAX_AGE + generation * 50
///          这为"长寿基因"提供了选择压力——活得更久的实体有更多复制机会。
constexpr int MAX_AGE              = 2000;

/// @brief 最大代数上限
/// @details 超过此代数的实体将无法复制。防止无限制进化导致的：
///          1. 基因组过度膨胀
///          2. 数值溢出
///          3. 性能退化
constexpr int MAX_GENERATION       = 10000;

// ============================================================================
// 👁️ 感知参数 — 环境信息获取能力
// ============================================================================

/// @brief 感知半径（使用 Chebyshev 距离度量）
/// @details SENSE 指令能探测此半径内的其他实体和食物分布。
///          Chebyshev 距离（棋盘距离）定义为 max(|dx|, |dy|)，
///          在环面上感知区域近似为一个 11x11 的正方形。
///          更大的感知范围让实体更"聪明"，但会使感知系统计算量增至 O(n²)。
constexpr int SENSE_RANGE          = 5;

} // namespace Config
